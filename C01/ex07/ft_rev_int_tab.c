#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	t;
	int	i;
	int	j;

	i = 0;
	j = size - 1;
	while (i < j)
	{
		t = tab[i];
		tab[i] = tab[j];
		tab[j] = t;
		i++;
		j--;
	}
}
