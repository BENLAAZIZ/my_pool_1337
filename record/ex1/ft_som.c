#include <stdio.h>

void	ft_som(int *a, int *b)
{
	int t;
	t = *a;
	*a = *a + *b;
	*b = t - *b;
}


int main(void)
{
	int	a;
	int	b;

	a = 100;
	b = 90;

	ft_som(&a, &b);
	printf("a = %d\n", a);
	printf("b = %d", b);
	return (0);
}
