#include <unistd.h>

void	ft_swap(int *a, int *b)
{
	int	t;

	t = *a;
	*a = *b;
	*b = t;
}

int	main(void)
{
	int	a;
	int	b;

	a = 4;
	b = 2;
	ft_swap(&a, &b);
	a = a + '0';
	b = b + '0';
	write(1, "a = ", 4);
	write(1, &a, 1);
	write(1, "\n", 1);
	write(1, "b = ", 4);
	write(1, &b, 1);
	return (0);
}
